    game_type = TRUMPF_DICT[0]()
    hand_cards = [Card.create(6, "HEARTS"), Card.create(7, "DIAMONDS")]
    table_cards = [{'number': 8, 'color': "CLUBS"}, {'number': 9, 'color': "SPADES"}]
    played_cards = [Card.create(10, "HEARTS"), Card.create(11, "DIAMONDS")]

